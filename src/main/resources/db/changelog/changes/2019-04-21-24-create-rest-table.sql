CREATE TABLE IF NOT EXISTS Rest
(
    id     int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    day_id int,
    time_id int,
    place_id int,
    CONSTRAINT R_DayId FOREIGN KEY (day_id)
        REFERENCES Day (id) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT R_TimeId FOREIGN KEY (time_id)
        REFERENCES Time (id) ON DELETE SET NULL ON UPDATE CASCADE,
    CONSTRAINT R_PlaceId FOREIGN KEY (place_id)
        REFERENCES RestPlace (id) ON DELETE SET NULL ON UPDATE CASCADE
);