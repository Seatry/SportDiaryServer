CREATE TABLE IF NOT EXISTS TrainingsToAims
(
    id     int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    aim_id int,
    training_id int,
    CONSTRAINT TTA_TrainingId FOREIGN KEY (training_id)
        REFERENCES Training (id) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT TTA_AimId FOREIGN KEY (aim_id)
        REFERENCES Aim (id) ON DELETE CASCADE ON UPDATE CASCADE
);